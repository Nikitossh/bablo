package com.company.bablo.forThinkingInJava.Chapter3;

public class BinaryString {

    public static void main(String[] args) {
        int i1 = 0x2f;
        System.out.println(Integer.toBinaryString(i1));
        Long l1 = 3286578965L;
        System.out.println(Long.toBinaryString(l1));
    }
}
